package com.hospital.backend.entity;

public class Specialization {
    private int spId;
    private String spName;
  
    
   
	public int getSpId() {
		return spId;
	}



	public void setSpId(int spId) {
		this.spId = spId;
	}



	public String getSpName() {
		return spName;
	}



	public void setSpName(String spName) {
		this.spName = spName;
	}



	// Default constructor
    public Specialization() {}
    
   
  
  
}